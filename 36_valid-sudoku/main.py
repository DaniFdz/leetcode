from collections import defaultdict
from typing import List


class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        rows = defaultdict(set)
        cols = defaultdict(set)
        sectors = defaultdict(set)
        for i in range(9):
            for j in range(9):
                val = board[i][j]
                if val == ".":
                    continue
                s = (i // 3) + (j // 3) * 3

                if val in rows[j] or val in cols[i] or val in sectors[s]:
                    return False

                rows[j].add(val)
                cols[i].add(val)
                sectors[s].add(val)

        return True


